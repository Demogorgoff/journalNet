#!/bin/sh

RESET='\033[0m'
BOLD_WHITE='\033[1;37m'
BOLD_YELLOW='\033[1;33m'
BOLD_BLUE='\033[1;94m'
BOLD_GRAY='\033[1;90m'

printf "${RESET}\n"
printf "${BOLD_WHITE}*                                     \n"
printf "${BOLD_YELLOW} (\.          ./)                   \n"  
printf "${BOLD_YELLOW}  \(   ${BOLD_BLUE}|\     ${BOLD_YELLOW})/                      \n"
printf "${BOLD_BLUE}  //\  | \   /\%s\                      \n"
printf "${BOLD_BLUE} (/ /\_${BOLD_WHITE}#${BOLD_YELLOW}oo${BOLD_WHITE}#${BOLD_BLUE}_/\ \)                     \n"
printf "${BOLD_BLUE}  \/\_ ${BOLD_WHITE}#### ${BOLD_BLUE}_/\/                      \n"
printf "${BOLD_WHITE}    _  ${BOLD_BLUE}\%s${BOLD_WHITE}##${BOLD_BLUE}/ ${BOLD_WHITE}_          _         _    \n"
printf "${BOLD_WHITE} __/\%s\__  _/\%s\___   __/\%s\___  __/\%s\__ \n"
printf "${BOLD_WHITE}(_    _))(_      ))(_  ____))(__  __))\n"
printf "${BOLD_WHITE}  \  \%s\   /  :   \%s\ /  ._))    /  \%s\  \n"
printf "${BOLD_GRAY}/\/ ${BOLD_WHITE}.:${BOLD_GRAY}\%s\ /${BOLD_WHITE}:. ${BOLD_GRAY}|   ///${BOLD_WHITE}:. ${BOLD_GRAY}||___  /${BOLD_WHITE}:.${BOLD_GRAY}  \%s\ \n"
printf "${BOLD_GRAY}\__  _// \___|  // \  _____)) \__  // \n"
printf "${BOLD_GRAY}   \//        \//   \//          \//  \n"
printf "${RESET}\n"                                                                            
