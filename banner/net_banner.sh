#!/bin/sh

RESET='\033[0m'
GRAY='\033[0;90m'
GREEN='\033[0;32m'
BOLD_GREEN='\033[1;32m'

printf "${RESET}\n"
printf "   ${GRAY}__ ______        __                    \n"
printf "  |  |   _  \\\\.-----|  |_  .------._   ___ \n"
printf "  |  |${BOLD_GREEN}.  ${GRAY}|   |  -__|   _  |${GREEN}\$_${GRAY}    | | |==||\n"
printf " _|  |${BOLD_GREEN}.  ${GRAY}|   |_____|____| |      | | |==||\n"
printf "|____|${BOLD_GREEN}:  ${GRAY}|   |            :______:/  |\"${GRAY}_|/\n"
printf "     ${GRAY}|${BOLD_GREEN}::.${GRAY}|   |           .-------.        \n"
printf "     '${GRAY}--- ---'          '======='  ()     \n"
printf "${RESET}\n"
